int color(int x){
    if (x == 0) return 231;
    else if (x == 6) return 52;
    else if (x == 7) return 82;
    else if (x == 12) return 11;
    else return 196;
}

void dote(int k) //新しく関数を作る
{
    printf("\e[H\e[J\e[?7l"); //画面消去

    if (k % 2 == 0)
    {
        printf("\n\n");
    }
    for (int i = 0; i < 16; i++){
        printf("%*s", k, ""); //k個の空白
        for (int j = 0; j < 16; j++){
            int x = a[i][j];
            if (k % 2 == 1) {
                x = b[i][j];
            }
            printf("\e[48;5;%dm", color(x));
            printf("  "); //printf("%2d", x)
            printf("\e[0m");
        }
        printf("\n");
    }
    usleep(125000); //0.125秒待つ
}

